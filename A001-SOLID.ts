/*
S. - Single Responsibility Principle(Princípio da responsabilidade única)
 - uma classe deve ter apenas um motivo para mudar
 - (evite conjuções aditivas: e, bem como, também...).
 - Este princípio está intimamente ligado com outro, conhecido como (Separation of concerns)

O. Open/closed principle (Princípio do aberto/fechado)
 - módulos, classes, objetos e operações devem estar abertos para extensão, mas fechados
   para modificações

L. Liskov substitution principle(Princípio da substituição de Liskov) -
 - Se o(x) é uma pripriedade desmonstrável dos objetos x de tipo T. Então o(x) deve
   ser verdadeito para objetos y de tipo S onde S é um subtipo de T.
 - (subtipos precisam ser substituíveis por seus tipos de base).

I. Interface segregation principle (Princípio da segregação de interface)
 - Os clinetes não devem ser forçados a depender de interfaces que não utilizam

D. Dependency inversion principle (Princípo da inversão de dependência)
 - Módulos de alto nível não devem ser dependentes do módulos de baixo nível
 - Ambos devem depender de abstrações.
 - Detalhes devem depender das abstrações, não o inverso.
*/
